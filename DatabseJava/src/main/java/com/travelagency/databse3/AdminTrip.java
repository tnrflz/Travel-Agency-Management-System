    private void ButTripSilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButTripSilMouseClicked
        // TODO add your handling code here:

        int SelectedRow = jTable1.getSelectedRow();

        tripid = Integer.parseInt((String.valueOf(jTable1.getModel().getValueAt(SelectedRow, 0)) ));


        PreparedStatement ps1;
        PreparedStatement ps2;
        try {
            String query1 = "DELETE FROM `manage` WHERE `Trip ID`=?";

            ps1 = NewClass.getConnection().prepareStatement(query1);
            ps1.setInt(1, (tripid));

            String query2 = "DELETE FROM `trip` WHERE `Trip ID`=?";

            ps2 = NewClass.getConnection().prepareStatement(query2);
            ps2.setInt(1, (tripid));


            if (ps1.executeUpdate() > 0) {

            //    JOptionPane.showMessageDialog(null, "");
            }
            if (ps2.executeUpdate() > 0) {

                JOptionPane.showMessageDialog(null, "Trip deleted");
            }
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.removeRow(SelectedRow);


            jTable1.setModel(model);

        }

        catch (Exception a) {
            System.out.println(a.getMessage());
        }


    }//GEN-LAST:event_ButTripSilMouseClicked

