    private void ButEkleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButEkleMouseClicked
        // TODO add your handling code here:

        

        int log = Login.Loginid;
        int agencyid=0;
        PreparedStatement ps1;
        ResultSet rs1;

        try {
        String query2 = "SELECT  `Agency ID` FROM `admin` WHERE `Login ID`=? ";

        ps1 = NewClass.getConnection().prepareStatement(query2);
        ps1.setInt(1, log);
        rs1=ps1.executeQuery();
        while (rs1.next()) {


            agencyid = rs1.getInt(1);
        }

            String query3 = "INSERT INTO `trip`( `Departure City`, `Arrival City`, `Departure Time`, `Arrival Time`, `Price`, `Trip Date`, `License Plate`, `Number of Seats`, `Agency ID`) VALUES (?,?,?,?,?,?,?,?,?)";


            ps1 = NewClass.getConnection().prepareStatement(query3);
            ps1.setString(1,TFDepCity.getText());
            ps1.setString(2,TFArrCity.getText());
            ps1.setString(3,TFDepTime.getText());
            ps1.setString(4,TFArrTime.getText());
            ps1.setInt(5, Integer.parseInt(TFPrice.getText()));
            ps1.setString(6,TFDate.getText());
            ps1.setString(7,TFPlaka.getText());
            ps1.setInt(8, Integer.parseInt(TFSeat.getText()));
            ps1.setInt(9, agencyid);

            if (ps1.executeUpdate() > 0) {

                JOptionPane.showMessageDialog(null, "New  trip added");
            }

        }

             catch (Exception a) {
            System.out.println(a.getMessage());
        }

        
    }//GEN-LAST:event_ButEkleMouseClicked

